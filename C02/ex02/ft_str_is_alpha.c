#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'Z')
		{
			i = i + 0;
		}
		else if ('a' <= str[i] && str[i] <= 'z')
		{
			i = i + 0;
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (1);
}
