int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('a' <= str[i] && str[i] <= 'z')
		{
			i = i + 0;
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (1);
}
