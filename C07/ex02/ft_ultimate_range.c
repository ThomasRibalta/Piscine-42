#include <stdlib.h>
#include <stdio.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;

	if (min >= max)
		return (0);
	i = min;
	while (i < max)
		i++;
	range[0] = (int *) malloc((i - min) * sizeof(int));
	if (!range[0])
		return (-1);
	i = 0;
	while (min < max)
	{
		range[0][i] = min;
		min++;
		i++;
	}
	return (i);
}
