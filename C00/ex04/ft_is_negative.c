#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	np;

	p = 'P';
	np = 'N';
	if (n >= 0)
	{
		write(1, &p, 1);
	}
	else
	{
		write(1, &np, 1);
	}
}
